// 常用类型
//布尔
const flag: boolean = true;
//数字
const num: number = 1;
// 字符串
const str: string = "larry";
// 数组
const arr: number[] = [1, 2, 3];
// 是一个对象
const obj: object = {}
// 偷懒做法，不考虑啥类型，就直接用这个
const everything: any = 1
// 元组
const tuple: [string, number] = ["hello", 1];

